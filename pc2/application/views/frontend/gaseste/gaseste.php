                    <p class="bold">Multimedia</p>

                    <p class="intins">Multimedia este un departament destinat oamenilor creativi, preocupaţi de informaţie şi actualitate, care doresc să inoveze programele din biserică, într-un mod pozitiv şi spiritual, mai ales în domeniul online.</p>
                    <p class="intins">Dumnezeu ne-a dat daruri diferite tocmai pentru ca împreună să desăvârşim o lucrare bună, să ne completăm în slujire cu pasiune şi muncă.</p>
                    <p class="intins">Nu trebuie decât să te implici cu seriozitate, să ai cunoştinte generale despre media, o predispoziţie definită spre acest domeniu şi restul învăţăm împreună. Iar dacă vom avea noi de învăţat de la tine, te aşteptăm cu multă bucurie! Suntem deschişi ideilor şi vrem să devenim cei mai buni în domeniul în care am ales să-L slujim pe Dumnezeu.</p>

                    <p class="intins">Pentru că în prezent mass-media şi internetul sunt la îndemâna tinerilor mai mult decât oricând, noi ştim că acest lucru poate avea atât un impact pozitiv cât şi unul negativ asupra lor. Ei pot alege ce să citească, să vizioneze sau să asculte atâta timp cât au de ales.</p>
                    <p class="intins">Departamentul multimedia din biserica Poarta Cerului doreşte să formeze o echipă de oameni care să contribuie la transmiterea mesajului creştin prin toate mijloacele mass media, pentru ca acest mesaj să fie aproape de oameni şi să facă parte din alegerile lor.</p>

                    <p class="intins">Dacă doreşti să faci parte din echipa media, asteptăm să ne contactezi accesând formularul de contact sau să iei legătura cu Daniel Ciupac. Pune-ţi ideile în practică!</p>
