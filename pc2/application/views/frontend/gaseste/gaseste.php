                <div class="ascuns">
                    <p class="bold">Misiune</p>

                    <p class="intins">“Duceţi-vă şi faceţi ucenici din toate neamurile, botezându-i în Numele Tatălui şi
                        al Fiului şi al Sfântului Duh. Şi învăţaţi-i să păzească tot ce v-am poruncit. Şi iată că Eu
                        sunt cu voi în toate zilele, până la sfârşitul veacului.” Matei 28:19-20
                    </p>

                    <p class="intins">Rolul Bisericii a fost, este şi va fi până la „sfârşitul veacului” acela de a-L duce pe Hristos în mijlocul lumii. Înţelegând că fără lucrarea de misiune îşi pierde scopul şi ţelul, biserica Poarta Cerului implică toate resursele umane, spirituale şi materiale de care este nevoie în proclamarea adevărului Evangheliei prin:</p>

                    <p> SCOP </p>

                    <p class="intins"> Noi credem că scopul lui Dumnezeu este acela de a avea oameni după chipul şi asemănarea Sa. Credem că scopul final al lucrării lui Cristos este acela de a "duce pe mulţi fii la slavă" (Evrei 2:10). </p>

                    <p class="intins"> Scopul nostru este acela de a mobiliza şi de a echipa tineri plini de pasiune pentru această lucrare, dornici să-L facă cunoscut pe Dumnezeu în zone în care sunt puţini credincioşi. Dorim ca tot mai mulţi oameni să fie atraşi de Cuvântul lui Dumnezeu şi să-L primească ca Mântuitor. </p>

                    <p> ACTIVITĂȚI </p>

                    <p class="intins"> Biserica Poarta Cerului se implică în formarea grupurilor de misiune în care sunt implicaţi tineri care înţeleg planul lui Dumnezeu pentru viaţa lor şi doresc să promoveze valorile creştine. Liderii acestor grupuri stabilesc întâlniri care îi ajută pe tineri să-L cunoască pe Dumnezeu mai bine, să crească în relaţia cu El, să trăiască mai mult ca El şi să-şi descopere chemarea şi darurile primite de la Dumnezeu pentru a le folosi în misiune. </p>

                    <p class="intins"> Activitatea de misiune nu presupune doar deplasarea grupurilor în diferite localităţi, la biserici cu un număr redus de membri în care nevoia de slujire este foarte mare ci şi implicarea în alte zone din ţară, în special Oltenia, prin proiecte pe termen lung sau scurt. </p>

                    <p class="intins"> Susţinem misiunea în Oltenia printr-o strânsă legătură cu familii de misionari stabiliţi în această zonă, prin activităţi propriu-zise de misiune (evanghelizare, lucrarea cu copiii, vizite la bolnavi), prin sprijin financiar acordat bisericilor aflate în construcţie şi prin multă rugaciune atât pentru echipele care pleacă în misiune cât şi pentru cei care aud Vestea Bună. </p>

                    <p> VIZIUNE </p>

                    <p class="intins"> Suntem convinşi că Dumnezeu ne-a chemat pe fiecare dintre noi să fim un misionar, să ducem Cuvântul Lui până la marginile pământului, să Îl facem cunoscut la orice persoană şi ştim că suntem toţi implicaţi în misiunea lui Dumnezeu de a-şi vesti dragostea şi de a-şi răscumpăra un popor. Vrem să fim o Biserică deschisă pentru cei nemântuiţi şi să folosim orice oportunitate pentru a câstiga suflete pentru Cristos. Pe viitor, dorim ca această lucrare să se dezvolte în special în localităţile din Oltenia. Pentru aceasta, ne propunem să formăm misionari care să ajute la plantarea de noi biserici, să iniţiem diverse acţiuni sociale (consiliere, vizite la bătrâni, asistenţă socială pentru copii, acţiuni caritabile) şi să atragem resurse financiare pentru sprijinirea construirii de biserici în zona Olteniei. </p>

                    <p class="intins"> Motivaţi de porunca Domnului de a merge şi a face ucenici, de holdele care sunt coapte dar şi de măreaţa răsplătire, haideţi să fim împreună harnici pentru Hristos şi să spunem: ”Sunt aici, stau la dispoziţia Ta”. </p>

