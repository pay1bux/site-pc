            <div class="i_details">
                <p class="bold">PREZENTARE</p>
                <p class="intins">Misiunea Creștină "Cristos pentru România", sub auspiciile căreia
                    functionează Școala Biblică a fost fondată în anul 1991 de către
                    un grup de absolvenți ai Institutului "Christ for the Nation" din
                    Dallas, SUA. </p>
                <p class="intins">Având o identitate clara în cadrul misiunii, Școala Biblică și-a
                    deschis porțile la Timișoara în 1992 unde și-a stabilit sediul
                    central.</p>
                <p class="intins">De la înființare și până în prezent numai la Timișoara au
                    frecventat cursurile peste 1100 de cursanți, din care majoritatea
                    au absolvit cel puțin programul de educație biblică de doi ani. </p>
                <p class="intins"> Mulți dintre absolvenți sunt implicați în bisericile locale prin
                    conducerea cu succes a grupelor de studiu biblic, organizarea de
                    misiuni evanghelistice, lucrarea cu copiii la Școala Duminicala și
                    coordonarea activităților muzicale din biserică. Alții lucrează ca
                    misionari cu baza permanentă în câmpul de misiune din Oltenia, ca
                    suplinitori în predarea religiei în școlile publice sau asistenți
                    sociali în orfelinate. Câțiva sunt implicați în lucrarea pastorală
                    iar alții sunt parte deja din staff-ul unor scoli biblice.</p>
                <p class="bold">OBIECTIVE</p>
                <p class="intins">Pentru a-și împlini scopul înzestrării și pregatirii lucrătorilor
                    creștini pentru slujire, Școala biblică caută să atingă
                    următoarele obiective: </p>
               <ul>
                   <li> să fie o instituție de educație creștină având ca scop slujirea
                       bisericilor locale pentru echiparea sfinților în vederea lucrării
                       de slujire</li>
                   <li> să ofere o programă școlară bazată pe Sfânta Scriptură care
                       recunoaște Biblia ca autoritate finală în reglementarea vieții de
                       credință și a comportamentului uman</li>
                   <li> să ofere o atmosferă spirituală în care studentul să aibă ocazia
                       să-și dezvolte întreaga personalitate ca bărbat sau femeie a lui
                       Dumnezeu și să încurajeze închinarea și rugăciunea pentru ca
                       studenții să aibă o viață călăuzită de Duhul Sfânt și îmbogățită
                       prin manifestarea darurilor Duhului Sfant.</li>
                   <li> să cultive un spirit misionar evanghelistic care îl va ajuta pe
                       student să se preocupe de cei care nu-L cunosc pe Cristos ca
                       Mântuitor personal și să-l ajute să nu precupețească niciun efort
                       pentru a-L face cunoscut pe Cristos</li>
                   <li> să stimuleze și să urmărească punerea în practică a celor
                       învățate prin implicarea studenților în multiplele activități ale
                       bisericilor locale și în diversele activități evanghelistice sau
                       de caritate.</li>
               </ul>
                <p class="bold">OFERTA NOASTRĂ</p>
                <p class="intins">
                    Pregătirea teologică și practică se desfășoara în cadrul a trei
                    programe distincte: </p>
                <p class="intins">
                   <span style="font-weight: bold;"> Educație biblică</span><br/>
                    Se studiază 32 de cursuri a căror tematică acoperă întreaga Biblie
                    pe durata a doi ani, cu frecvență redusă, în fiecare zi de luni și
                    vineri, orele 18:00 - 21:00. Se poate înscrie în acest program
                    orice membru cu drepturi depline al unei biserici locale, care
                    dorește să studieze Cuvantul lui Dumnezeu. </p>
                <p class="intins">
                    <span style="font-weight: bold;">Teologie practică</span><br />
                    Se studiază 24 de cursuri avansate, la nivel de colegiu biblic, pe
                    o durată de doi ani, în fiecare zi de luni și vineri, orele 18:00
                    - 21:00. Se adresează numai absolvenților programului de Educație
                    Biblică.</p>
                <p class="intins">
                   <span style="font-weight: bold;">Seminarul Biblic Penticostal</span> <br/>
                    Este o combinație a primelor două programe și se desfășoară pe
                    durata a doi ani cu întâlniri în fiecare prima sâmbătă a lunii.
<br/>
                    Acest program include 54 de cursuri care se studiază pe module
                    lunare de trei cursuri și este recunoscut de Comunitatea
                    Penticostală Arad.
                    <br/>
                    Se adresează atât fraților cât și surorilor care sunt deja
                    implicați în lucrarea spirituală (păstori, prezbiteri, diaconi,
                    învățători, lideri de tineret, învățători de copii, etc.), precum
                    și celor care simt chemarea lui Dumnezeu pentru lucrare. Cei
                    interesați de acest program sunt așteptați sș se înscrie având o
                    recomandare scrisă din partea păstorului sau a conducătorului
                    bisericii locale din care face parte.
                </p>
                <p class="intins">
                   <span style="font-style: italic;">Predarea cursurilor se face de către profesori licențiați în
                    teologie, care ocupă și poziții de slujire în diferite biserici
                    locale, având astfel avantajul de a se raporta în mod
                    corespunzător la nevoile spirituale actuale din țara noastră.</span>
                </p>
