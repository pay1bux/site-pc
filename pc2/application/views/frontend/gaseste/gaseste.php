                <div class="ascuns">
                    <p class="bold">Misiune</p>

                    <p class="intins">“Duceţi-vă şi faceţi ucenici din toate neamurile, botezându-i în Numele Tatălui şi
                        al Fiului şi al Sfântului Duh. Şi învăţaţi-i să păzească tot ce v-am poruncit. Şi iată că Eu
                        sunt cu voi în toate zilele, până la sfârşitul veacului.” Matei 28:19-20
                    </p>

                    <p class="intins">Rolul Bisericii a fost, este și va fi până la „sfârșitul veacului” acelea de a-L
                        duce pe Hristos în mijlocul lumii. Înţelegând că fara lucrarea de misiune își pierde scopul și
                        țelul, biserica Poarta Cerului implică toate resursele umane, spirituale şi materiale de care
                        este nevoie în proclamarea adevărului Evangheliei prin: </p>

                    <p> SCOP </p>

                    <p class="intins"> Noi credem ca scopul lui Dumnezeu cu omul este acela de a avea oameni după chipul
                        şi asemanarea Sa. Credem că scopul final al lucrării lui Cristos este acela de a "duce pe multi
                        fii la slavă" (Evrei 2:10). </p>

                    <p class="intins"> Scopul nostru este acela de a mobiliza şi echipa tineri plini de pasiune pentru
                        această lucrare, dornici să-L facă cunoscut pe Dumnezeu în zone în care sunt puțini credincioși.
                        Dorim ca tot mai mulţi oameni să fie atrasi de Cuvantul lui Dumnezeu şi să-L primească ca
                        Mantuitor. </p>

                    <p> ACTIVITĂȚI </p>

                    <p class="intins"> Biserica Poarta Cerului se implică în formarea grupurilor de misiune în care sunt
                        implicați tineri din biserică care inţeleg planul lui Dumnezeu pentru viaţa lor şI doresc să
                        promoveze valorile creştine. Liderii acestor grupuri stabilesc întâlniri care ii ajută pe tineri
                        să’L cunoască pe Dumnezeu mai bine, să crească în relaţia cu El, să trăiască mai mult ca El şi
                        să-si descopere chemarea şi darurile primite de la Dumnezeu pentru a le folosi in misiune. </p>

                    <p class="intins"> Activitatea de misiune nu presupune doar deplasarea grupurilor în diferite
                        localități, la biserici cu un număr redus de membri în care nevoia de slujire este foarte mare
                        ci şi implicarea în alte zone din ţară, în special Oltenia, prin proiecte pe termen lung sau
                        scurt. </p>

                    <p class="intins"> Susţinem misiunea în Oltenia printr-o strânsă legătură cu familii de misionari
                        stabiliţi în această zonă, prin activităţi propriu-zise de misiune (evanghelizare, lucrarea cu
                        copiii, vizite la bolnavi), prin sprijin financiar acordat bisericiilor aflate în construcţie şi
                        prin multă rugaciune atât pentru echipele care pleacă în misiune cât şi pentru cei care aud
                        Vestea Bună. </p>

                    <p> VIZIUNE </p>

                    <p class="intins"> Suntem convinși că Dumnezeu ne-a chemat pe fiecare dintre noi să fim un misionar,
                        să ducem Cuvântul Lui până la marginile pământului, să Îl facem cunoscut la orice persoană și
                        știm că suntem toți implicați în misiunea lui Dumnezeu de a-Și vesti dragostea și de a-Și
                        răscumpăra un popor. Vrem să fim o Biserica deschisă pentru cei nemântuiţi şi să folosim orice
                        oportunitate pentru a câstiga suflete pentru Cristos. Pe viitor, dorim ca această lucrare să se
                        dezvolte în special în localităţiile din Oltenia. Pentru aceasta, ne propunem să formăm
                        misionari care să ajute la plantarea de noi bisericii, să iniţiem diverse acţiuni sociale
                        (consiliere, vizite la bătrâni, asistenţă socială pentru copii, acţiuni caritabile) şi să
                        atragem resurse financiare pentru sprijinirea construirii de biserici în zona Olteniei. </p>

                    <p class="intins"> Motivaţi de porunca Domnului de a merge şi a face ucenici, de holdele care sunt
                        coapte dar şi de măreața răsplătire, haideti să fim împreuna harnici pentru Hristos si să
                        spunem: ”Sunt aici, stau la dispoziția Ta”.
                    </p>

