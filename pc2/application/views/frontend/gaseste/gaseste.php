                <div class="ascuns">
                    <p class="bold">Multimedia</p>

                    <p class="intins">Multimedia este un departament destinat oamenilor creativi care doresc să aibă un
                        impact pozitiv asupra celorlalți, chiar dacă nu știu să cânte, să recite sau să predice.
                        Dumnezeu ne-a dat talente diferite ca să ne completăm unii pe alții în lucrarea Lui.
                        Nu trebuie decât să te implici cu seriozitate, să ai cunoștințe generale despre media, o
                        predispoziție definită spre acest domeniu și restul învățam împreună.
                        Iar dacă vom avea noi de învățat de la tine, te așteptăm cu multă bucurie! Suntem deschiși
                        ideilor și vrem să devenim cei mai buni în domeniul în care am ales să-L slujim pe El.
                    </p>

                    <p class="intins">Pentru că mass-media și internetul poate avea un impact pozitiv sau negativ asupra
                        omului, acest lucru depinzând de alegerea lui. Dar dacă noi nu venim cu un mesaj creștin în
                        media și pe internet, lui nu-i rămâne decât să aleagă ceea ce există deja, sau mai bine zis - nu
                        are de ales.
                        Noi dorim să transmitem mesajul Lui Dumnezeu prin toate mijloacele posibile pentru ca oamenii să
                        facă alegeri bune și să fie informați în domeniile care îi interesează.
                    </p>

                    <p class="intins">Dacă dorești să te implici, așteptăm sș ne scrii folosind formularul de mai jos
                        sau să ne vizitezi la departamenul media din biserică unde poți întreba de Daniel Ciupac.
                    </p>
