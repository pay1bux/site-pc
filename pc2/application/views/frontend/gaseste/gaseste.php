                <div class="ascuns">
                <p class="bold">Multimedia</p>

                <p>Multimedia este un departament destinat oamenilor creativi care doresc sa aiba un impact pozitiv asupra celorlalti, chiar daca nu stiu sa cante, sa recite sau sa predice. Dumnezeu ne-a dat talente diferite ca sa ne completam unii pe altii in lucrarea Lui.
                    Nu trebuie decat sa te implici cu seriozitate, sa ai cunostinte generale despre media, o predispozitie definita spre acest domeniu si restul invatam impreuna.
                    Iar daca vom avea noi de invatat de la tine, te asteptam cu multa bucurie! Suntem deschisi ideilor si vrem sa devenim cei mai buni in domeniul in care am ales sa-L slujim pe El.

                 </p>
                <p>Pentru ca mass-media si internetul poate avea un impact pozitiv sau negativ asupra omului, acest lucru depinzand de alegerea lui. Dar daca noi nu venim cu un mesaj crestin in media si pe internet, lui nu-i ramane decat sa aleaga ceea ce exista deja, sau mai bine zis - nu are de ales.
                    Noi dorim sa transmitem mesajul Lui Dumnezeu prin toate mijloacele posibile pentru ca oamenii sa faca alegeri bune si sa fie informati in domeniile care ii intereseaza.

                </p>
                <p>Daca doresti sa te implici, asteptam sa ne scrii folosind formularul de mai jos sau sa ne vizitezi la departamenul media din biserica unde poti intreba de Daniel Ciupac.
</p>
