                <div class="ascuns">
                    <p class="bold">Școala duminicală</p>

                    <p class="intins">“Învaţă pe copil calea pe care trebuie să o urmeze şi când va îmbătrâni, nu se va abate de la ea.” Proverbe 22:6 </p>
                    <p class="intins">Copiii de azi vor forma familiile şi Biserica de mâine, de aceea împreună cu părinţii dorim să contribuim la clădirea şi consolidarea unor caractere frumoase care să devină o binecuvântare atât pentru oameni, cât şi pentru Dumnezeu.</p>
                    <p class="intins">Şcoala Duminicală e locul unde mulţi dintre copii fac deja primii paşi pe calea credinţei, se hotărăsc în a-L urma pe Hristos şi îşi conturează o viziune în ceea ce priveşte viitorul lor în lucrarea Domnului.</p>
                    <p class="intins">Şcoala Duminicală se desfasoară duminica, concomitent cu orarul slujbelor din biserica. Astfel, copiii sunt aşteptaţi dimineaţa între orele 9.00 – 12.00 si seara între orele 18.00 – 20.00. Copiii sunt împărţiţi pe categorii de vârstă după cum urmează: grupa mică (cu vârsta cuprinsă între 2-4 ani), grupa mijlocie (cu vârsta cuprinsă între 5-7 ani), grupa mare (cu vârsta cuprinsă între 8-11 ani) şi grupa de adolescenţi.</p>
                    <p class="intins">Biserica Poarta Cerului e interesată de lucrarea cu copiii şi oferă sprijin acestei lucrări, în primul rând prin existenţa unui spaţiu special amenajat pentru desfăşurarea programului şi oferă posibilitatea organizării activitătilor într-un mod personal în funcţie de vârsta copiilor, de cunoştinţele lor intelectuale şi cele spirituale, şi nu în ultimul rand punând la dispoziţie diverse resurse materiale.</p>
                    <p class="intins">Învăţătorii de la fiecare grupă înţeleg importanţa implicării în slujirea copiilor prin prezentarea Bibliei pe întelesul lor. Scopul activităţilor de la fiecare grupă este acela de a ajuta la transformarea şi creşterea spirituală a copiilor, mai mult decât dobândirea unor cunoştinte. Ceea ce noi sădim acum în ei va aduce rod odată cu trecerea anilor.</p>
                    <p class="intins">Dumnezeu ne-a chemat pe toţi să îl slujim. O parte a acestei slujiri este şi lucrarea cu copiii. Dacă eşti unul dintre cei chemaţi să slujeşti copiilor, eşti aşteptat să te alături celorlalţi colegi cu aceeaşi chemare. Dacă nu ştii ce chemare ai, dar vrei să încerci în acest domeniu, eşti binevenit!</p>
                    <p class="intins">Persoana de contact:  Avram Sabou - Copii, învăţători, vă aşteptăm cu drag!</p>
