                <p class="bold">PREZENTARE</p>
                <p class="intins">Misiunea Crestina "Cristos pentru Romania", sub auspiciile careia
                    functioneaza Scoala Biblica a fost fondata in anul 1991 de catre
                    un grup de absolventi ai Institutului "Christ for the Nation" din
                    Dallas, SUA. </p>
                <p class="intins">Avand o identitate clara in cadrul misiunii, Scoala Biblica si-a
                    deschis portile la Timisoara in 1992 unde si-a stabilit sediul
                    central.</p>
                <p class="intins">De la infiintare si pana in prezent numai la Timisoara au
                    frecventat cursurile peste 1100 de cursanti, din care majoritatea
                    au absolvit cel putin programul de educatie biblica de doi ani. </p>
                <p class="intins"> Multi dintre absolventi sunt implicati in bisericile locale prin
                    conducerea cu succes a grupelor de studiu biblic, organizarea de
                    misiuni evanghelistice, lucrarea cu copiii la Scoala Duminicala si
                    coordonarea activitatilor muzicale din biserica. Altii lucreaza ca
                    misionari cu baza permanenta in campul de misiune din Oltenia, ca
                    suplinitori in predarea religiei in scolile publice sau asistenti
                    sociali in orfelinate. Cativa sunt implicati in lucrarea pastorala
                    iar altii sunt parte deja din staff-ul unor scoli biblice.</p>

                <p class="bold">OBIECTIVE</p>
                <p class="intins">Pentru a-si implini scopul inzestrarii si pregatirii lucratorilor
                    crestini pentru slujire, Scoala biblica cauta sa atinga
                    urmatoarele obiective: </p>
               <ul>
                   <li> sa fie o institutie de educatie crestina avand ca scop slujirea
                       bisericilor locale pentru echiparea sfintilor in vederea lucrarii
                       de slujire</li>
                   <li> sa ofere o programa scolara bazata pe Sfanta Scriptura care
                       recunoaste Biblia ca autoritate finala in reglementarea vietii de
                       credinta si a comportamentului uman</li>
                   <li> sa ofere o atmosfera spirituala in care studentul sa aiba ocazia
                       sa-si dezvolte intreaga personalitate ca barbat sau femeie a lui
                       Dumnezeu si sa incurajeze inchinarea si rugaciunea pentru ca
                       studentii sa aiba o viata calauzita de Duhul Sfant si imbogatita
                       prin manifestarea darurilor Duhului Sfant.</li>
                   <li> sa cultive un spirit misionar evanghelistic care il va ajuta pe
                       student sa se preocupe de cei care nu-L cunosc pe Cristos ca
                       mantuitor personal si sa-l ajute sa nu precupeteasca nici un efort
                       pentru a-l face cunoscut pe Cristos</li>
                   <li> sa stimuleze si sa urmareasca punerea in practica a celor
                       invatate prin implicarea studentilor in multiplele activitati ale
                       bisericilor locale si in diversele activitati evanghelistice sau
                       de caritate.</li>

               </ul>

                <p class="bold">OFERTA NOASTRA</p>
                <p class="intins">
                    Pregatirea teologica si practica se desfasoara in cadrul a trei
                    programe distincte: </p>
                <p class="intins">
                   <span style="font-weight: bold;"> Educatie biblica</span><br/>
                    Se studiaza 32 de cursuri a caror tematica acopera intreaga Biblie
                    pe durata a doi ani, cu frecventa redusa, in fiecare zi de luni si
                    vineri, orele 18:00 - 21:00. Se poate inscrie in acest program
                    orice membru cu drepturi depline al unei biserici locale, care
                    doreste sa studieze Cuvantul lui Dumnezeu. </p>


                <p class="intins">
                    <span style="font-weight: bold;">Teologie practica</span><br />
                    Se studiaza 24 de cursuri avansate, la nivel de colegiu biblic, pe
                    o durata de doi ani, in fiecare zi de luni si vineri, orele 18:00
                    - 21:00. Se adreseaza numai absolventilor programului de Educatie
                    Biblica.</p>

                <p class="intins">
                   <span style="font-weight: bold;">Seminarul Biblic Penticostal</span> <br/>
                    Este o combinatie a primelor doua programe si se desfasoara pe
                    durata a doi ani cu intalniri in fiecare prima sambata a lunii.
<br/>
                    Acest program include 54 de cursuri care se studiaza pe module
                    lunare de trei cursuri si este recunoscut de Comunitatea
                    Penticostala Arad.
                    <br/>
                    Se adreseaza atat fratilor cat si surorilor care sunt deja
                    implicati in lucrarea spirituala (pastori, prezbiteri, diaconi,
                    invatatori, lideri de tineret, invatatori de copii, etc.), precum
                    si celor care simt chemarea lui Dumnezeu pentru lucrare. Cei
                    interesati de acest program sunt asteptati sa se inscrie avand o
                    recomandare scrisa din partea pastorului sau a conducatorului
                    bisericii locale din care face parte.
                </p>

                <p class="intins">
                   <span style="font-style: italic;">Predarea cursurilor se face de catre profesori licentiati in
                    teologie, care ocupa si pozitii de slujire in diferite biserici
                    locale, avand astfel avantajul de a se raporta in mod
                    corespunzator la nevoile spirituale actuale din tara noastra.</span>
                </p>

