            <div class="i_title">Gaseste-ți locul în Biserica Poarta Cerului</div>
            <div class="i_details">
                <p class="intins">Noi credem că Dumnezeu a pus în fiecare din noi un dar special pentru binecuvântarea
                    celor din jurul nostru și pentru slava Lui. Tu știi care este acel dar și locul unde Dumnezeu vrea
                    să-l folosești? Așteptăm să-ți găsești locul în biserică, în unul din departamentele prezentate mai
                    jos.
                </p>

                <p class="mic intins" style="margin-bottom: 30px;">Atunci când vei găsi locul inimii tale, al
                    intelectului tău, al sufletului și spiritului tău, te vei afla la locul potrivit. În acel moment,
                    oriunde ai fi, oricare ar fi situația ta materială și socială, vei simți ca locul tău se găsește
                    acolo. Te vei simți împlinit!</p>

                <div class="ascuns">
                    <p class="bold">Muzică</p>

                    <p class="intins">“Neprihăniţilor, bucuraţi-vă în Domnul! Oamenilor fără prihană le şade bine
                        cântarea de laudă. Lăudaţi pe Domnul cu arfa, lăudaţi-L cu alăuta cu zece coarde. Cântaţi-I o
                        cântare nouă! Faceţi să răsune coardele şi glasurile voastre.” Psalmul 33:1-3
                    </p>

                    <p class="intins"> Încă din cele mai vechi timpuri, muzica a fost considerată un element vital în
                        închinarea ce trebuie adusă înaintea lui Dumnezeu. Aşa au gândit si cei doi dirijori, aflând
                        viziunea pe care Dumnezeu a dat-o fratelui pastor Nelu Filip, aceea de a deschide o nouă
                        biserică penticostală în Timişoara.</p>

                    <p class="intins"> Odată cu înfiinţarea bisericii Poarta Cerului în decembrie 2005, fraţii Teo &
                        Remus Alazăroae au pus bazele corului mixt, împreună cu un grup restrâns de tineri creştini,
                        dedicaţi lucrării la care au fost chemaţi. De-a lungul anilor, acestui grup s-au alăturat şi
                        alţi tineri care au simţit chemarea de a-L sluji pe Dumnezeu prin cântare.</p>

                    <p class="intins"> Este un lucru de dorit şi plăcut înaintea Domnului să fim cu inima gata pentru
                        a-L lăuda! Fiind convinşi de acest adevăr, coriştii bisericii Poarta Cerului işi pun la
                        dispozitia Domnului, inima, talentul, puterea, timpul şi intreaga viaţă.</p>

                    <p class="intins"> În viaţa bisericii Poarta Cerului muzica corului are un rol deosebit de
                        important. Prin stilul pe care îl abordează si prin mesajele diverse (evanghelizare, mângâiere,
                        bucurie), cântările corului îi ajută pe cei prezenţi să intre într-o atmosferă de închinare şi
                        părtăşie cu Dumnezeu. Conştienţi de responsabilitatea slujirii lor în biserică prin susţinerea
                        închinării, rugaciunea corului este ca prin ungerea Duhului Sfânt, fiecare cântare sa fie spre
                        Slava Domnului, a zidirii bisericii şi a apropierii de Domnul a celor care o ascultă.</p>

                    <p class="intins"> Iubind cântarea, cu ajutorul Domnului, al dirijorilor şi al fiecărui membru în
                        parte, corul bisericii caută să exceleze mereu şi mereu în calitate şi în manifestarea dragostei
                        faţă de Cel care merită toată slava, cinstea şi onoarea!</p>

                    <p class="intins"> Dacă simţi chemarea de a-L sluji pe Dumnezeu în acest mod, ai talent şi o inimă
                        bună, dacă eşti gata să fii responsabil în această slujire şi doreşti sa te implici, poţi lua
                        legatura cu dirijorii:
                    </p>
